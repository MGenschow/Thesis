class Labels():
    def __init__(self, dataset_name: str):

        if dataset_name.startswith('zalando'):
            self.task_mapping = zalando_label_mappings[dataset_name]
            self.task_label_dims = [len(c) for c in self.task_mapping.values()]
        else:
            raise ValueError(f'No labels specified for dataset \'{dataset_name}\'')

    def get_task_index(self, task: str) -> int:
        return list(self.task_mapping.keys()).index(task)